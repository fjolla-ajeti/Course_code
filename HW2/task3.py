#Make a program with 2 numbers saved in separate variables a and b, then print the result for each of the following: 

#Addition
#Subtraction
#Division
#Multiplication
#Exponent (Power)
#Modulus
#Floor division


a = 10
b = 3

# Addition
result_addition = a + b
print(f"Addition: {a} + {b} = {result_addition}")

# Subtraction
result_subtraction = a - b
print(f"Subtraction: {a} - {b} = {result_subtraction}")

# Division
result_division = a / b
print(f"Division: {a} / {b} = {result_division}")

# Multiplication
result_multiplication = a * b
print(f"Multiplication: {a} * {b} = {result_multiplication}")

# Exponent (Power)
result_exponent = a ** b
print(f"Exponent (Power): {a} ** {b} = {result_exponent}")

# Modulus
result_modulus = a % b
print(f"Modulus: {a} % {b} = {result_modulus}")

# Floor division
result_floor_division = a // b
print(f"Floor Division: {a} // {b} = {result_floor_division}")
